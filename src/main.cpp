/**
 * Runs initialization code. This occurs as soon as the program is started.
 *
 * All other competition modes are blocked by initialize; it is recommended
 * to keep execution time for this mode under a few seconds.
 */
void initialize() {
	pros::lcd::initialize();
	
}

/**
 * Runs while the robot is in the disabled state of Field Management System or
 * the VEX Competition Switch, following either autonomous or opcontrol. When
 * the robot is enabled, this task will exit.
 */
int current_auton_selection = 0;
bool auto_started = false;
void disabled() {}

/**
 * Runs after initialize(), and before autonomous when connected to the Field
 * Management System or the VEX Competition Switch. This is intended for
 * competition-specific initialization routines, such as an autonomous selector
 * on the LCD.
 *
 * This task will exit when the robot is enabled and autonomous or opcontrol
 * starts.
 */
void auto_plus(void){
  current_auton_selection ++;
  clamp(current_auton_selection, 0, 8);
}
void auto_minus(void){
  current_auton_selection --;
  clamp(current_auton_selection, 0, 8);
}
void competition_initialize() {
	// Initializing Robot Configuration. DO NOT REMOVE!
  default_constants();

  while(auto_started == false){            //Changing the names below will only change their names on the
    pros::lcd::clear();            //brain screen for auton selection.
    switch(current_auton_selection){       //Tap the brain screen to cycle through autons.
      case 0:
        pros::lcd::print(1, "Drive Test");
        break;
      case 1:
        pros::lcd::print(1, "Drive Test");
        break;
      case 2:
        pros::lcd::print(1, "Turn Test");
        break;
      case 3:
        pros::lcd::print(1, "Swing Test");
        break;
      case 4:
        pros::lcd::print(1, "Full Test");
        break;
      case 5:
        pros::lcd::print(1, "Odom Test");
        break;
      case 6:
        pros::lcd::print(1, "Tank Odom Test");
        break;
      case 7:
        pros::lcd::print(1, "Holonomic Odom Test");
        break;
    }
    pros::lcd::register_btn0_cb(auto_plus);
    pros::lcd::register_btn2_cb(auto_minus);
    pros::Task::delay(10);
  }
}

/**
 * Runs the user autonomous code. This function will be started in its own task
 * with the default priority and stack size whenever the robot is enabled via
 * the Field Management System or the VEX Competition Switch in the autonomous
 * mode. Alternatively, this function may be called in initialize or opcontrol
 * for non-competition testing purposes.
 *
 * If the robot is disabled or communications is lost, the autonomous task
 * will be stopped. Re-enabling the robot will restart the task, not re-start it
 * from where it left off.
 */
void autonomous() {
	 auto_started = true;
  switch(current_auton_selection){  
    case 0:
      drive_test(); //This is the default auton, if you don't select from the brain.
      break;        //Change these to be your own auton functions in order to use the auton selector.
    case 1:         //Tap the screen to cycle through autons.
      drive_test();
      break;
    case 2:
      turn_test();
      break;
    case 3:
      swing_test();
      break;
    case 4:
      full_test();
      break;
    case 5:
      odom_test();
      break;
    case 6:
      tank_odom_test();
      break;
    case 7:
      // /*holonomic_odom_test()*/;
      break;
 }
}

